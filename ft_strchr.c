#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (c > 255 || c < 0)
		return (NULL);
	while (*s)
	{
		if (*s == c)
			return ((char *)s);
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (NULL);
}
